#include <iostream>
using namespace std;

int main()
{
    //转义字符

    //换行符\n
    cout << "helloworld\n";

    //反斜杠 \\
    
    cout << "\\" << endl;

    //水平制表符 \t
    cout << "aaa\t helloworld" << endl;
    cout << "aa\t helloworld" << endl;
    cout << "aaaaa\t helloworld" << endl;

    return 0;
}